def tokenize_ipa(text, ignore_short=True):
    """
    Splits an IPA string into its constituent characters.
      This primarily prevents diacritics from being treated as 
      separate characters and being split off from the characters they modify.
    """
    text = text.replace("\xa0", "") + "%"
    text = strip_supra(text)
    text = re.sub("<.*?>", "", text)
    for s1, s2 in substitutions:
        text = text.replace(s1, s2)
    tokens = []
    i = 0
    n = len(text)
    while i < n:
      char = text[i]
      i += 1
      if char in chars and i < n:
        next = text[i]
        while (next in diacs) and (i < n):
          char += next
          i += 1
          next = text[i]
        tokens.append(char)
      elif char in standalone_diacs: 
        tokens.append(standalone_dict[char])
        i += 1
      else: 
        pass
        if char != "%":
            print(f"Character {char} in word {text} not in list of allowed characters.")
    return tokens

